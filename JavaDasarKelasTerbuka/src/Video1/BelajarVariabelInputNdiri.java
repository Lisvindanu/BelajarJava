package Video1;

import java.util.Scanner;

public class BelajarVariabelInputNdiri {
    public static void main(String[] args) {

        Scanner Input = new Scanner(System.in);
        // versi input ndiri
        // deklarasi variabel
        //tipe data
        int a, b, c;

        //memberi nilai ke variabel




        //menampilkan nilai ke terminal
        System.out.print("Nilai variabel a : " );
        a = Input.nextInt();
        System.out.print("Nilai variabel b : "  );
        b = Input.nextInt();

        c = a + b;
        System.out.println("Nilai variabel c : " + c);

    }

}
