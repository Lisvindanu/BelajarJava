package OperatorAssignments;

public class Main {
    public static void main(String[] args) {
        //pertambahan
        int a = 100;
        a += 10;
        System.out.println("a = " + a);
        //pengurangan
        int b = 100;
        b -= 10;
        System.out.println("b = " + b);
        //perkalian
        int c = 100;
        c *= 10;
        System.out.println("c = " + c);
        //pembagian
        int d = 100;
        d /= 10;
        System.out.println("d = " + d);
        //modulo
        int e = 10;
        e %= 7;
        System.out.println("e = " + e);
    }
}
